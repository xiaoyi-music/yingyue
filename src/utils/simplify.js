// 反向构建正则：匹配所有繁体字
const regex = new RegExp(Object.keys(t2sMap).join('|'), 'g')

/**
 * 将字符串中的繁体中文转换为简体中文
 * @param {string} str
 * @returns {string}
 */
export function simplify(str) {
  if (!str) return ''
  return str.replace(regex, (char) => t2sMap[char] || char)
}

/**
 * 简化歌曲信息（递归处理 name, artist, album）
 * @param {Object} song
 * @returns {Object}
 */
export function simplifySong(song) {
  if (!song) return song
  return {
    ...song,
    name: simplify(song.name),
    artist: Array.isArray(song.artist)
      ? song.artist.map(simplify)
      : simplify(song.artist),
    album: simplify(song.album),
  }
}
